public class Looping {
    public static void main(String[] args) {
        // LOOPING FOR 
        System.out.println("tolong baca KOMEN BOSSS!");

        for (int i=0; i < 5; i++){
            System.out.println("Urutan For ke-" + i);
        }

        //LOOPING WHILE
        int k = 5;
        int i = 1;
        while (i<k){

            System.out.println("Urutan While ke-" + i++);

        }
    }
}

