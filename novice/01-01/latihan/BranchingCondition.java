public class BranchingCondition {
    public static void main(String[] args) {
        boolean kiri= false;
        boolean kanan = true;
        if (kanan){
            System.out.println("BENAR");
        }
        else{
            System.out.println("SALAH");
        }

        if (kiri){
            System.out.println("Bener");
        }
        else{
            System.out.println("SALAH");
        }
    }
}